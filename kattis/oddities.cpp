#include<stdio.h>
#include<iostream>
#include<math.h>
int main()
{
    int t;
    if(scanf("%d",&t)==1);
    while(t--)
    {
        int n;
        if(scanf("%d",&n)==1);
        if(n%2==0)
            printf("%d is even\n",n);
        else
            printf("%d is odd\n",n);
    }
    return 0;
}
